a, b = map(int,input().split())
c = int(input())
if c % 60 == 0: #더하는 수가 60의 배수일 때
    if ((b + c) // 60) > 0: #분의 합을 60으로 나누었을 때 몫이 0보다 클 때
        print((a + ((b + c) // 60) % 24), (b + (c % 60)) % 60)
    else:
        print((a + ((b + c) // 60)) % 24, (b + (c % 60)) % 60)
else: #더하는 수가 60의 배수가 아닐 때
    if ((b + c) // 60) > 0: #분의 합을 60으로 나누었을 때 몫이 0보다 클 때
        print((a + (b + c) // 60) % 24, (b + (c % 60)) % 60)
    else:
        print((a + ((b + c) // 60)) % 24, (b + (c % 60)) % 60)
